#include "libft.h"

int	ft_str_isnum(char *str)
{
	int	i;

	i = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		++i;
		if (str[i] == '\0')
			return (1);
	}
	return (0);
}
